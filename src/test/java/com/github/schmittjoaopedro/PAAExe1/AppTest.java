public class AppTest {

    public static void main(String[] args) {
        System.out.println("for(int i = 8; i > 0; i--)");
        for(int i = 8; i > 0; i--) {
            System.out.println("Teste");
            System.out.println(i);
        }
        System.out.println("for(int i = 0; i < 8; i++)");
        for(int i = 0; i < 8; i++) {
            System.out.println("Teste");
            System.out.println(i);
        }
    }

}
